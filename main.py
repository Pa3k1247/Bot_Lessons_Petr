async def send_video_cached(chat_id: int, path: str, button):
    """–û—Ç–ø—Ä–∞–≤–∫–∞ –≤–∏–¥–µ–æ —Å –∫—ç—à–∏—Ä–æ–≤–∞–Ω–∏–µ–º file_id –∏ –∫–Ω–æ–ø–∫–æ–π, —Å—Ä–∞–∑—É —Å –∫–Ω–æ–ø–∫–æ–π"""
    if path in video_cache:
        await bot.send_video(
            chat_id,
            video_cache[path],
            width=1080,
            height=2340,
            supports_streaming=True,
            reply_markup=button
        )
    else:
        sent_video = await bot.send_video(
            chat_id,
            FSInputFile(path),
            width=1080,
            height=2340,
            supports_streaming=True,
            reply_markup=button
        )
        video_cache[path] = sent_video.video.file_id



